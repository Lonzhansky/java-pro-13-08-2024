package ua.hillel.lessons.lesson26.grasp.demos._7_pure_fabrication;

public class Product {

    private String name;
    private int id;

    Product(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
